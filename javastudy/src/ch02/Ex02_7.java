package ch02;

public class Ex02_7 {

	public static void main(String[] args) {
		System.out.println("\'a\'>\'b\'");
		System.out.println('a'>'b');
		System.out.println();
		
		System.out.println("3>=2");
		System.out.println(3>=2);
		System.out.println();
		
		System.out.println("-1<0");
		System.out.println(-1<0);
		System.out.println();
		
		System.out.println("3.45<=2");
		System.out.println(3.45<=2);
		System.out.println();
		
		System.out.println("3==2");
		System.out.println(3==2);
		System.out.println();
		
		System.out.println("3 !=2");
		System.out.println(3 !=2);
		System.out.println();
		
		System.out.println("!(3!=2)");
		System.out.println(!(3!=2));
		System.out.println();
		
		System.out.println("(3>2) && (3>4)");
		System.out.println((3>2) && (3>4));
		System.out.println();
		
		System.out.println("(3!=2)||(-1>0)");
		System.out.println((3!=2)||(-1>0));
		System.out.println();
		
		System.out.println("(3!=2)^(-1>0)");
		System.out.println((3!=2)^(-1>0));
	}
}
